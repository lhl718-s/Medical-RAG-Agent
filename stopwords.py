# stopwords.py

# 这是一个基础的中文医疗停用词表
STOPWORDS = {
    "的", "了", "在", "是", "我", "有", "和", "就", "不", "人", "都", "一", "一个", "上", "也", "很", "到", "说", "要", "去", "你", "会", "着", "没有", "看", "好", "自己", "这",
    "关于", "如何", "怎么", "怎么办", "什么", "为什么", "哪些", "哪种", "请问", "谢谢", "医生", "建议", "咨询", "由于", "导致", "因此", "如果", "但是", "虽然", "因为", "所以","啊","吧","哦","呢"
}

def filter_stopwords(tokens):
    """过滤掉列表中的停用词"""
    return [t for t in tokens if t not in STOPWORDS and len(t) > 1] # 顺便过滤掉长度为1的无效单字